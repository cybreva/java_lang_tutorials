public class operators {
    public static void main (String[] args){
        int a = 10;
        int b = 3;
    //arithmatic operators {+  -  *  /  %}
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    //relational operator {==  !=  >  <  >=  <=}
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);

    //Logical Operators {// && || !}

        System.out.println(a > 5 && b > 15);
        System.out.println(a > 15 || b > 15);
        System.out.println(!(a > b));

    }
}
