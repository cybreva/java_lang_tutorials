public class ifelse {
    public static void main( String[] args) {


        int age = 100 ;


        if (age >= 1100000) {
            System.out.println("are you god");
        }
        else if (age >= 10912) {
            System.out.println("you're literally older than the whole universe");
        }
        else if (age >= 100) {
            System.out.println("are you really that old");
        }
        else if (age == 0) {
            System.out.println("welcome to the world");
        }
        else {
            System.out.println("are you yet to be born??");
        }


        }
    }

